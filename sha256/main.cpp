#include <iostream>
#include <chrono>
#include <ctime>
#include "SHA256.h"

int main(int argc, char ** argv) {
  char buf[64] = {0x1,0x2,0x3,0x4,0x5,0x6, 0x7,0x8, 
  0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8, 
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,
   0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8};
		SHA256 sha;
		sha.update(buf);
		uint8_t * digest = sha.digest();

		std::cout << SHA256::toString(digest) << std::endl;
		delete[] digest;

	return EXIT_SUCCESS;
}
